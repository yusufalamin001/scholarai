'use client'

import Link from 'next/link'
import { usePathname } from 'next/navigation'

const TABS = [
  { label: 'Courses', href: '/courses', icon: CoursesIcon },
  { label: 'Progress', href: '/progress', icon: ProgressIcon },
  { label: 'Quiz', href: '/quiz', icon: QuizIcon },
  { label: 'Rooms', href: '/rooms', icon: RoomsIcon },
  { label: 'Planner', href: '/planner', icon: PlannerIcon },
]

export default function BottomNav() {
  const pathname = usePathname()

  return (
    <nav className="md:hidden fixed bottom-0 left-0 right-0 bg-[#0D1117] border-t border-[#1C2235] z-40">
      <div className="flex items-center justify-around px-2 py-2">
        {TABS.map(tab => {
          const isActive = pathname === tab.href ||
            (tab.href !== '/courses' && pathname.startsWith(tab.href))
          return (
            <Link
              key={tab.label}
              href={tab.href}
              className={`flex flex-col items-center gap-1 px-3 py-1 rounded-xl transition-colors ${
                isActive ? 'text-[#0EA5E9]' : 'text-[#475569]'
              }`}
            >
              <tab.icon className="w-5 h-5" />
              <span className="text-[10px] font-medium">{tab.label}</span>
            </Link>
          )
        })}
      </div>
    </nav>
  )
}

